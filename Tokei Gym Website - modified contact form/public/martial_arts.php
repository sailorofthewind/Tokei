		<p>Aiki Ju Jitsu, San Shou Kuan, Judo, Karate, Olympic Freestyle Wrestling and Thai Boxing are amongst the list of timetabled Martial Arts classes. Some of these classes take place at lunch time so why not join us then.</p>
		
		<h3>Aiki Ju Jitsu</h3>
		<p>A method of self-defence without weapons that was developed in China and Japan; holds and blows are supplemented by clever use of the attacker's own weight and strength.</p>

		<h3>San Shou Kuan</h3>
		<p>Chinese Kickboxing, the ancient art of ultimate self-defence and total fitness. The aim of this art is to progressively train your body as well as your mind to achieve positive health and to acquire effective skill to defend yourself at all times. Training with various weapons is taught only to the more advanced students who have already acquired a solid grounding</p>

		<h3>Judo</h3>
		<p>The Japanese for 'Gentle Art'. It is a self-defence and a sport, involving gripping, throwing and holding including arm-locks and strangles for the more senior players. It builds strength, fitness and confidence. All instructors are qualified through the British Judo Association. Children are taught from the age of six years and upwards.</p>

		<h3>Karate</h3>
		<p>This word is a combination of the Japanese terms 'kara' which means empty and 'te' which means hand. Added with the Japanese 'do' meaning path or way, giving Karate-do or 'empty hand way'. This is an art that uses various parts of the body, through continuous and methodical training, to develop them into powerful weapons that can be effective in defence or attack. 
For the last 30 years the <a href="http://cargocollective.com/tokeikyokushinkai" target="_blank">Tokei Kyokushinkai Karate Dojo</a> has been dedicated to teaching the hand-to-hand combat style of Kyokukushinkai Karate. The Tokei Dojo has produced accomplished fighters who have fought on the world stage, but above all our goal is to instil in our students the skills of self-defence, physical fitness and mental focus that comes from adopting Kyokushin into their lifestyles.</p>

		<h3>Olympic Freestyle Wrestling</h3>
		<p>A derivative of ancient Greco-Roman wrestling developed into a sport using both arms and legs whereby competitors attempt to throw their opponent to the ground and pin them to the mat using established set techniques. This is a combative sport not to be confused with populist television wrestling.</p>

		<h3>Thai Boxing</h3>
		<p>The ancient art of Thailand. The fighting art consists of many elements; these include using the knees and elbows, low and high kicks. Combined all together is a force to be reckoned with.</p>

		<h3>Other Associated Martial Arts</h3>
		<p>Aikido, Brazillian Jiu Jitsu and Sanjuro.</p>

